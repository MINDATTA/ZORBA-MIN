DROP FUNCTION DBO.UDF_NET_SALES

CREATE FUNCTION DBO.UDF_NET_SALES(@QUANTITY INT, @LIST_PRICE DEC(10,2),@DISCOUNT DEC(4,2))
RETURNS DEC(10,2)
AS 
BEGIN
    RETURN (@QUANTITY * @LIST_PRICE * (1 - @DISCOUNT))
END


