DROP FUNCTION DBO.MOBILE
CREATE FUNCTION DBO.MOBILE (@MOBILE VARCHAR(11))
RETURNS VARCHAR(11)
AS
BEGIN
DECLARE @PHONE VARCHAR(11)
SET @PHONE=@MOBILE
IF((@PHONE LIKE'%[0-9]%') AND LEN(@PHONE)=10)
BEGIN
SET @PHONE=@MOBILE
END

ELSE
BEGIN
SET @PHONE=NULL

END
RETURN @PHONE
END
--EXECUTING THE FUNCTION

